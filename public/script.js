document.getElementById("connect-wallet").addEventListener("click", connectWallet);

async function connectWallet() {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: "eth_requestAccounts" });
        const accounts = await web3.eth.getAccounts();
        document.getElementById("wallet-address").innerText = "Connected: " + accounts[0];
        contract = new web3.eth.Contract(contractABI, contractAddress);
    } else {
        alert("Please install MetaMask!");
    }
}

async function createRoom() {
    const name = document.getElementById("roomName").value;
    executeTransaction(contract.methods.createRoom(name));
}

async function joinRoom() {
    const roomId = document.getElementById("joinRoomId").value;
    executeTransaction(contract.methods.joinRoom(roomId));
}

async function leaveRoom() {
    const roomId = document.getElementById("leaveRoomId").value;
    executeTransaction(contract.methods.leaveRoom(roomId));
}

async function addTask() {
    const roomId = document.getElementById("taskRoomId").value;
    const description = document.getElementById("taskDesc").value;
    executeTransaction(contract.methods.addTask(roomId, description));
}

async function completeTask() {
    const roomId = document.getElementById("completeRoomId").value;
    const taskIndex = document.getElementById("taskIndex").value;
    executeTransaction(contract.methods.completeTask(roomId, taskIndex));
}

async function getRoomMembers() {
    const roomId = document.getElementById("membersRoomId").value;
    showLoading();
    try {
        const members = await contract.methods.getRoomMembers(roomId).call();
        document.getElementById("room-members").innerText = "Members: " + members.join(", ");
    } catch (error) {
        console.error(error);
        alert("Error fetching members.");
    }
    hideLoading();
}

async function loadRooms() {
	const result = await contract.methods.showListRoom().call();
	const ids = result[0];
	const names = result[1];
	const memberCounts = result[2];

	let html = "";
	for (let i = 0; i < ids.length; i++) {
		html += `<tr>
					<td>${ids[i]}</td>
					<td>${names[i]}</td>
					<td>${memberCounts[i]}</td>
				 </tr>`;
	}
	document.getElementById("roomList").innerHTML = html;
}

// Hàm xử lý giao dịch chung, hiển thị loading khi chờ
async function executeTransaction(method) {
    const accounts = await web3.eth.getAccounts();
    showLoading();
    try {
        await method.send({ from: accounts[0] });
        alert("Transaction successful!");
    } catch (error) {
        console.error(error);
        alert("Transaction failed.");
    }
    hideLoading();
}



// Hiển thị hiệu ứng loading
function showLoading() {
    document.getElementById("loading").classList.remove("hidden");
}

// Ẩn hiệu ứng loading
function hideLoading() {
    document.getElementById("loading").classList.add("hidden");
}
